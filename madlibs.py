#madlibs sstory game
#taking input from the user
food = input("Enter a type of food: ")
name = input("Enter a boy's name: ")
adj1 = input("Enter an adjective: ")
bird = input("Enter a noun: ")
verb1 = input("Enter a verb in the past tense: ")
verb2 = input("Enter another verb in the past tense: ")
verb3 = input("Enter a third verb in the past tense: ")
noun1 = input("Enter a noun: ")
#story which.. user have to fill
story = "It was " + food + " day at school, and " + name + " was super " + adj1 + " for lunch. But when he/she went outside to eat, a " + food + " stole her " + food + "! " + name + " chased the " + bird + " all over school. She " + verb1 + ", " + verb2 + ", and " + verb3 + " through the playground. Then she tripped on her " + noun1 + " and the " + bird + " escaped! Luckily, " + name + "'s friends were willing to share their " + food + " with her."
print(story)
